package es.iestetuan.ngg.vo;


public class Color {

	private int codColor;
	private String nombre;
	private String codHexadecimal;
	
	
	public int getCodColor() {
		return codColor;
	}
	public void setCodColor(int codColor) {
		this.codColor = codColor;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCodHexadecimal() {
		return codHexadecimal;
	}
	public void setCodHexadecimal(String codHexadecimal) {
		this.codHexadecimal = codHexadecimal;
	}
	
}
